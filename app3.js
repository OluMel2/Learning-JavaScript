//引数は「,」でつないで複数の指定も可能です。複数指定すると、それぞれ順番に引数が格納される。
//関数の結果を出力したいときは、return内に戻り値を渡す。

//function 関数名(引数){
//  処理
//  return 戻り値;
//}

