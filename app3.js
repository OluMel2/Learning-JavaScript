//関数とは
//"関数" は、同じ処理をひとまとめにして再利用しやすくする方法
//処理内容に変更があった場合でも関数の中を変更するだけでいいので、保守性が高い。
//「関数（function）」は、繰り返し使われる一連の処理を1つにまとめたもの
//関数の形で定義しておき、毎回同じプログラムを書く代わりに、この関数を呼び出すことで処理を実行する。

//簡単な関数の形
//function 関数名(){
//  処理内容
//}

//JavaScriptでは、functionに続けて関数名を書き、{ }内に処理内容を記述する


